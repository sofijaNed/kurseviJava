    private void btnVidiSrednjuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVidiSrednjuActionPerformed
        // TODO add your handling code here:
        int selected = tableKur.getSelectedRow();
        int brojac = 0;
        int ocene = 0;
        double srednja = 0;
        boolean flag = false;
        if(selected >= 0){
        
            try {
                TableModelKurs tmk = (TableModelKurs) tableKur.getModel();
                k = tmk.vratiKurs(selected);
                ArrayList<Clanstvo> clanstva = Communication.getInstance().getAllClanstvo();
                for (Clanstvo clanstvo : clanstva) {
                    if(clanstvo.getGrupa().getKurs().getKursID()==k.getKursID()){
                    
                        if(clanstvo.getOcena()!= 0){
                        
                            flag= true;
                            brojac++;
                            ocene += clanstvo.getOcena();
                        }
                    }
                }
                if(flag){
                
                    srednja = (double)ocene/brojac;
                    txtSrednja.setText(Double.toString(srednja));
                }else{
                
                    JOptionPane.showMessageDialog(this, "Kurs jos nije ocenjen.");
                }
            } catch (IOException ex) {
                Logger.getLogger(PocetnaPolaznik.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
        
            JOptionPane.showMessageDialog(this, "Niste izabrali kurs.");
        }
    }//GEN-LAST:event_btnVidiSrednjuActionPerformed

