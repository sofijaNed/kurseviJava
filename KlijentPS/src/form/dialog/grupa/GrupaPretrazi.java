    private void popuniKombo() {
        try {
            ArrayList<Kurs> kursevi = Communication.getInstance().getAllKurs();
            cmbKurs.removeAllItems();
            for (Kurs kurs : kursevi) {
                cmbKurs.addItem(kurs);
            }
        } catch (IOException ex) {
            Logger.getLogger(GrupaPretrazi.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    private void popuniTabelu() {
        try {
            grupe = Communication.getInstance().getAllGrupe();
            TableModelGrupe tmg = (TableModelGrupe) tableGrupe.getModel();
            tmg.dodajGrupe(grupe);
        } catch (IOException ex) {
            Logger.getLogger(GrupaPretrazi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
