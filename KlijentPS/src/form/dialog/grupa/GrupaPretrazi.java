    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        // TODO add your handling code here:
        Kurs kurs = (Kurs) cmbKurs.getSelectedItem();
        ArrayList<Grupa> poKursevima = new ArrayList<>();
        
        for (Grupa grupa : grupe) {
            if(grupa.getKurs().getKursID()==kurs.getKursID()){
            
                poKursevima.add(grupa);
            }
        }
        if(poKursevima.isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe grupe po zadatoj vrednosti.");
            return;
        }
        TableModelGrupe tmg = (TableModelGrupe) tableGrupe.getModel();
        tmg.dodajGrupe(poKursevima);
        JOptionPane.showMessageDialog(this, "Sistem je našao grupe po zadatoj vrednosti.");
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnDetaljnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljnoActionPerformed
        // TODO add your handling code here:
        int selected = tableGrupe.getSelectedRow();
        if(selected >= 0){
        
            TableModelGrupe tmg = (TableModelGrupe) tableGrupe.getModel();
            Grupa gr = tmg.vratiGrupu(selected);
            PocetnaZaposleni pz = (PocetnaZaposleni) getParent();
            JOptionPane.showMessageDialog(this, " Sistem je učitao grupu.");
            new GrupaPrikazi(pz, false, gr).setVisible(true);
        }else{
        
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita grupu. Nije selektovan nijedna grupa.");
        }
    }//GEN-LAST:event_btnDetaljnoActionPerformed

