    void srediFormu(Kurs k) {
        txtKursID.setText(k.getKursID() + "");
        txtKursID.setEditable(false);
        txtNaziv.setText(k.getNaziv());
        txtNaziv.setEditable(false);
        txtImePrezimePredavaca.setText(k.getImePrezimePredavaca());
        txtImePrezimePredavaca.setEditable(false);
        txtVremeTrajanja.setText(k.getVremeTrajanja() + "");
        txtVremeTrajanja.setEditable(false);
        cmbOblast.getModel().setSelectedItem(k.getOblast());
        cmbOblast.setEditable(false);
        txtNazivLek.setEditable(false);
        
        cmbLekcije.removeAllItems();
        for(Lekcija lek: k.getLekcije()){
        
            cmbLekcije.addItem(lek);
        }
        
        btnDodaj.setEnabled(false);
        btnObrisi.setEnabled(false);
        btnKreiraj.setEnabled(false);
        btnSacuvaj.setEnabled(false);
        btnOtkazi.setEnabled(true);
    }

    
