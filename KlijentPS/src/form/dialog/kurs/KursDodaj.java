    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        // TODO add your handling code here:
        if(txtNazivLek.getText().isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Mora se upisati naziv lekcije!");
            return;
        }
        String nazivLekcije = txtNazivLek.getText();
        Lekcija lek = new Lekcija(-1, null, nazivLekcije);
        lekcije.add(lek);
        
        cmbLekcije.addItem(lek);
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        // TODO add your handling code here:
        Lekcija l = (Lekcija) cmbLekcije.getSelectedItem();
        lekcije.remove(l);
        cmbLekcije.removeAllItems();
        
        for (Lekcija lekcija : lekcije) {
            cmbLekcije.addItem(lekcija);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajActionPerformed
        try {
            // TODO add your handling code here:
            btnSacuvaj.setEnabled(true);
            Kurs kurs = new Kurs();
            kurs.setNaziv("");
            kurs.setOblast("");
            kurs.setVremeTrajanja(0);
            kurs.setImePrezimePredavaca("");
            Kurs ku = Communication.getInstance().createKurs(kurs);
            if(ku==null){
            
                JOptionPane.showMessageDialog(this, "Sistem ne moze da kreira kurs.");
            }else{
            
                txtKursID.setText(Integer.toString(ku.getKursID()));
                JOptionPane.showMessageDialog(this, "Sistem je kreirao kurs.");
                
            }
        } catch (IOException ex) {
            Logger.getLogger(KursDodaj.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnKreirajActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            // TODO add your handling code here:
            int id = Integer.parseInt(txtKursID.getText());
            if(txtNaziv.getText().isEmpty() || txtImePrezimePredavaca.getText().isEmpty() || txtVremeTrajanja.getText().isEmpty()){
            
                JOptionPane.showMessageDialog(this, "Svako polje mora biti popunjeno.");
                return;
            }
            String naziv = txtNaziv.getText();
            String imePr = txtImePrezimePredavaca.getText();
            int vremeTr = Integer.parseInt(txtVremeTrajanja.getText());
            if(vremeTr < 1){
            
                JOptionPane.showMessageDialog(this, "Vreme trajanja kursa ne sme biti manje od jednog dana.");
                return;
            }
            String oblast = (String) cmbOblast.getSelectedItem();
            Kurs kur = new Kurs(id, naziv, imePr, vremeTr, oblast);
            for (Lekcija lekcija : lekcije) {
                lekcija.setKurs(kur);
            }
            kur.setLekcije(lekcije);
            Object uspesno = Communication.getInstance().updateKurs(kur);
            if((boolean)uspesno){
            
                 JOptionPane.showMessageDialog(this, "Sistem je zapamtio kurs.");
                 btnSacuvaj.setEnabled(false);
//                 Object o;
//                 for (Lekcija lekcija : lekcije) {
//                     lekcija.setKurs(kur);
//                    o = Communication.getInstance().addLekcija(lekcija);
//                    if(!(boolean)o){
//                    
//                        JOptionPane.showMessageDialog(this, "Sistem nije uspeo da sacuva lekciju.");
//                    }
//                }
                 
            }else{
            
                JOptionPane.showMessageDialog(this, "Sistem ne moÅ¾e da zapamti kurs.");
            }
        } catch (IOException ex) {
            Logger.getLogger(KursDodaj.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

