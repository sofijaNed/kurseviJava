    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        // TODO add your handling code here:
        String oblast = (String) cmbOblast.getSelectedItem();
        ArrayList<Kurs> poOblastima = vratiListuPoOblastima(oblast);
        if(poOblastima.isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe kurseve po zadatoj vrednosti.");
        }
        TableModelKurs tmk = (TableModelKurs) tableKurs.getModel();
        tmk.dodajKurseve(poOblastima);
        JOptionPane.showMessageDialog(this, "Sistem je našao kurseve po zadatoj vrednosti.");
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selected = tableKurs.getSelectedRow();
        if(selected >= 0){
        
            try {
                TableModelKurs tmk = (TableModelKurs) tableKurs.getModel();
                Kurs k = tmk.vratiKurs(selected);
                ArrayList<Lekcija> lekcije = Communication.getInstance().getAllLekcija();
                ArrayList<Lekcija> lekZaKurs = new ArrayList<>();
                for (Lekcija lekcija : lekcije) {
                    if(lekcija.getKurs().getKursID()==k.getKursID()){
                    
                        lekZaKurs.add(lekcija);
                    }
                }
                k.setLekcije(lekZaKurs);
                PocetnaZaposleni pz = (PocetnaZaposleni) getParent();
                JOptionPane.showMessageDialog(this, "Sistem je učitao kurs.");
                KursDodaj kd = new KursDodaj(pz, false);
                kd.srediFormu(k);
                kd.setVisible(true);
            } catch (IOException ex) {
                Logger.getLogger(KursPretrazi.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
        
            JOptionPane.showMessageDialog(this, "Sistem ne moze da učita kurs. ");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

