    private void popuniTabelu() {
        try {
            kursevi = Communication.getInstance().getAllKurs();
            TableModelKurs tmk = (TableModelKurs) tableKurs.getModel();
            tmk.dodajKurseve(kursevi);
        } catch (IOException ex) {
            Logger.getLogger(KursPretrazi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private ArrayList<Kurs> vratiListuPoOblastima(String oblast) {
        ArrayList<Kurs> kurse = new ArrayList<>();
        for (Kurs kurs : kursevi) {
            if(kurs.getOblast().equals(oblast)){
            
                kurse.add(kurs);
            }
        }
        return kurse;
    }
